class product {
	constructor(id, sku, order_id, name, product_id, variation_id, quantity, sale_price, price, total, total_tax) {
		this.id = id;
		this.sku = sku;
		this.order_id = order_id;
		this.name = name;
		this.product_id = product_id;
		this.variation_id = variation_id;
		this.quantity = quantity;
		this.sale_price = sale_price;
		this.price = price;
		this.total = total;
		this.total_tax = total_tax;
	}
}